<template>
  <div class="login-container">
    <el-form ref="loginForm" :model="loginForm" :rules="loginRules" class="login-form" label-position="left">

      <div class="title-container">
        <h3 class="title">Login</h3>
      </div>

      <el-form-item prop="email">
        <span class="svg-container">
          <svg-icon icon-class="email" />
        </span>
        <el-input
          ref="email"
          v-model="loginForm.email"
          placeholder="edu/ac E-mail"
          type="email"
          tabindex="1"
          autocomplete="on"
        />
      </el-form-item>

      <el-form-item prop="password">
        <span class="svg-container">
          <svg-icon icon-class="password" />
        </span>
        <el-input
          ref="password"
          v-model="loginForm.password"
          type="password"
          placeholder="password"
          tabindex="2"
          autocomplete="on"
          @keyup.enter.native="handleLogin"
        />
      </el-form-item>

      <el-button :loading="loading" type="primary" style="width:100%;margin-bottom:30px;" @click.native.prevent="handleLogin">Login</el-button>

      <div class="tips">
        <router-link to="/signup">Don't have an account? Click here to Signup!</router-link>
      </div>

    </el-form>
  </div>
</template>

<script>

export default {
  name: 'Login',
  data() {
    const checkPass = (rule, value, callback) => {
      if (/^([A-Za-z]+|[0-9]+)$/.test(value) || !/^[A-Za-z0-9]{8,20}$/.test(value)) {
        callback(new Error('The password must contain both letters and numbers, and be 8-20 characters'))
      } else {
        callback()
      }
    }
    return {
      loginForm: {
        email: '',
        password: ''
      },
      loginRules: {
        email: [{
          required: true,
          type: 'email',
          message: 'Please enter the edu/ac E-mail',
          trigger: 'blur'
        }, {
          pattern: /^[\w-]{3,15}@([a-z0-9]+\.)+(edu|ac)\.[a-z]+$/,
          message: 'Please enter the edu/ac E-mail',
          trigger: 'blur'
        }],
        password: [{
          required: true,
          message: 'Please enter the password',
          trigger: 'blur'
        }, {
          validator: checkPass,
          trigger: 'blur'
        }]
      },
      loading: false,
      redirect: undefined
    }
  },
  watch: {
    $route: {
      handler: function(route) {
        this.redirect = route.query && route.query.redirect
      },
      immediate: true
    }
  },
  methods: {
    handleLogin() {
      this.$refs.loginForm.validate(valid => {
        if (valid) {
          this.loading = true
          this.$store.dispatch('user/login', this.loginForm).then(() => {
            this.$router.push({ path: this.redirect || '/' })
            this.loading = false
          }).catch(() => {
            this.loading = false
          })
        } else {
          console.log('Error submit!!')
          return false
        }
      })
    }
  }
}
</script>

